package com.wildace.roulette.websocket;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.web.socket.TextMessage;
import org.springframework.web.socket.WebSocketSession;
import org.springframework.web.socket.handler.TextWebSocketHandler;

public class TopperWebSocketHandler extends TextWebSocketHandler {

    private static final Logger logger = LoggerFactory.getLogger(TopperWebSocketHandler.class);

    @Override
    public void handleTextMessage(WebSocketSession session, TextMessage message) throws Exception {
        logger.info("Topper WebSocket received message: {}", message.getPayload());
        // Handle topper-specific WebSocket logic here
        session.sendMessage(new TextMessage("Topper response: " + message.getPayload()));
    }
}
